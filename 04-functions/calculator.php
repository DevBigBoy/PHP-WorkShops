<?php

// add
function add($num1, $num2){
    return $num1 + $num2;
}

// Subtration
function sub($num1, $num2){
    return $num1 - $num2;
}

// multiply
function multiply($num1, $num2){
    return $num1 * $num2;
}

// division
function div($num1, $num2){
    return $num1 / $num2;
}



?>