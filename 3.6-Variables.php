<h1>Manipulating Dynamic Data Using Variables</h1>
<p>A variable is a symbol that can store different values at different times</p>


<h3>Variable Declaration</h3>
<p>A variable always begins with a dollar sign, $, which is then followed by a variable name</p>


<?php
// $color
// • $operating_system
// • $_some_variable
// • $model
// And a few examples of invalid variable names:
// • $ color
// • $'test'
// • $-some-variable
?>
<p>Note that variables are case sensitive. For instance, the following variables bear no relation to one another</p>

<ul>
    <li>$color</li>
    <li>$Color</li>
    <li>$COLOR</li>
</ul>